package com.whm0304.exec;

public class VarB {
	
	
	public static void main(String[] args) {
		int num1 = 30;
		int num2 = 40;
		float num3 = 30.2f;
		float num4 = 40.3f;
		
		
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println(num1 / num2);
		System.out.println(num1 % num2);
		
		System.out.println(num3 + num4);
		System.out.println(num3 - num4);
		System.out.println(num3 * num4);
		System.out.println(num3 / num4);
		System.out.println(num3 % num4);
		
		
		
		
	}

}
